fn main() {
    //inumeros_de_bits
    //i64 = 64 bits
    //u64 = não permite números negativos
    let number: u64 = 23; //i64
    //f32 = 32 bits para float
    let numero_flutuante: f64 = 20.50;
    //booleano == bool
    let booleano: bool = true;

    println!("{},{},{}",number,numero_flutuante,booleano);
}
